#include "iostream"
using namespace std;
int main()
{
    float num_1, num_2;
    cout<<"Enter the first number: ";
    cin>>num_1;
    cout<<"Enter the second number: ";
    cin>>num_2;
    cout<<"Sum = "<<num_1+num_2<<"\n";
    cout<<"Difference = "<<num_1 - num_2<<"\n";
    cout<<"Product = "<<num_1*num_2<<"\n";
    cout<<"Quotient = "<<num_1/num_2<<"\n"<<endl;
    return 0;
}